// - є сторінка rules.html. Контентом сторінки є заголовки та параграфи. Заголовки (h2) характеризують тему контенту яка вказана в параграфі.
//     створити скріпт, котрий зчитує всі заголовки, та робить в блоці з id=content з них список(ul>li), який буде змістом того, що знаходиться на сторінці.
//     Скріпт повинен працювати навіть якщо кількість блоків з заголовком та параграфом зміниться.
const divIdContent = document.getElementById('content');
const ulElement = document.createElement('ul');
const h2Items = document.getElementsByTagName('h2');
for (const h2Item of h2Items) {
    const liElement = document.createElement('li');
    liElement.innerText = h2Item.innerText;
    ulElement.appendChild(liElement);
}

divIdContent.appendChild(ulElement);



// -Є масив котрий характеризує правила. Створити скрипт який ітерує цей масив, та робить з кожне правило в окремому блоці.
//     При цому в блоці, номер правила записати в свій блок, текст правила записати в свій окремий блок.
//     Результатом відпрацювання скріпта повинна бути структура яка міститься в блоці wrap файла rule.html
const divElement = document.createElement('div');
divElement.setAttribute('id','wrap2');

rules.forEach((item, index) => {
    const div = document.createElement('div');
    div.classList.add('rules');
    index++;
    div.classList.add('rule' + index);
    const h2Item = document.createElement('h2');
    h2Item.innerText = item.title;
    const pItem = document.createElement('p');
    pItem.innerText = item.body;
    divElement.appendChild(div);
    div.append(h2Item, pItem);
});

document.body.appendChild(divElement);
